/**
 * Create a Set for O(1) lookup performance
 */
const RESERVED_WORDS = new Set(reservedWords.map(word => word.toLowerCase()));

/**
 * Check if an alias is reserved
 * @param {string} alias - The alias to check
 * @returns {boolean} - True if reserved, false otherwise
 */
export const isReservedWord = (alias) => {
    if (!alias) return false;
    return RESERVED_WORDS.has(alias.toLowerCase());
};

/**
 * Get all reserved words (for documentation or admin purposes)
 * @returns {string[]} - Array of all reserved words
 */
export const getReservedWords = () => [...reservedWords];

/**
 * Get count of reserved words
 * @returns {number} - Total count of reserved words
 */
export const getReservedWordCount = () => RESERVED_WORDS.size;

export default reservedWords;
